// Fonction pour changer la langue
function changeLanguage(lang) {
    // Mettre à jour tous les éléments avec l'attribut data-translate
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        // Utiliser email_form pour le formulaire si la clé est 'email' et l'élément est un label ou input du formulaire
        if (key === 'email' && element.closest('.application-form')) {
            if (translations[lang]['email_form']) {
                element.innerHTML = translations[lang]['email_form'];
            }
        } else if (translations[lang][key]) {
            element.innerHTML = translations[lang][key];
        }
    });

    // Mettre à jour l'attribut lang de l'élément html
    document.documentElement.setAttribute('lang', lang);

    // Mettre à jour le contenu des popups des groupes
    for (const groupId in groupesData) {
        if (groupesData.hasOwnProperty(groupId)) {
            const contentKey = `group_content_${groupId.replace(/-/g, '_')}`; // ex: group_content_continentale_auto
            if (translations[lang][contentKey]) {
                groupesData[groupId].content = translations[lang][contentKey];
            }
            // Mettre à jour l'introduction du popup RSE
            if (groupId === "rse" && translations[lang].rse_popup_intro) {
                groupesData[groupId].intro = translations[lang].rse_popup_intro;
            }
        }
    }

    // Sauvegarder la préférence de langue
    localStorage.setItem('preferredLanguage', lang);
}

